# int型
num = 1234; print(num)

# 8進数
num = 0o777; print(num)

# 16進数
num = 0xffff; print(num)

# 2進数
num = 0b11000100; print(num)

# long型はint型に統合されている
# num = 1L; print(num)

# 浮動小数点数
num = 1.234; print(num)
num = 1.2e3; print(num)
num = 1.2e-3; print(num)

# 虚数
num = 3.14j; print(num)

# 論理値
bool = True; print(bool)
bool = False; print(bool)

# 文字列
str = "Hello"; print(str)
str = 'world'; print(str)
str = "Hello \
    world"; print(str)

# エスケープシーケンス
str = "aaa\nbbb"; print(str)
str = r"aaa\nbbb"; print(str)

# 文字列の結合
print("Hello " "world")

# 文字コード
len = len("あいうえお"); print(len)

#文字列のフォーマット
print("%s" % "ABC")
print("%d" % 123)
print("%f" % 1.23)
print("%x" % 255)
print("%o" % 255)
print("%%%d" % 12)



